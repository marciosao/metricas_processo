CREATE TABLE issues (
  id INT,
  subject VARCHAR(255),
  custom_fields TEXT,
  created_on DATETIME,
  updated_on DATETIME,
  tracker_name VARCHAR(255),
  status_id INT,
  status_name VARCHAR(255),
  priority_name VARCHAR(255),
  author_name VARCHAR(255),
  assigned_name VARCHAR(255),
  category_name VARCHAR(255),
  parent_id INT,
  fiplan_20 BOOLEAN,
  legado BOOLEAN,
  gestor VARCHAR(255),
  time_fiplan VARCHAR(100),
  impacto_bdce VARCHAR(100),
  agrupamento_demanda VARCHAR(255),
  complexidade_demanda VARCHAR(100),
  pf_estimado FLOAT,
  origem_mudanca VARCHAR(255),
  versao_caso_uso VARCHAR(20),
  data_prevista_desenvolvimento DATETIME,
  data_entrega DATETIME,
  origem_defeito VARCHAR(100),
  backlog BOOLEAN,
  projeto BOOLEAN,
  data_prevista_teste DATETIME,
  rastro_fiplan TEXT,
  esforco FLOAT,
  data_entrega_homologacao DATETIME,
  data_prevista_implantacao DATETIME,
  classificacao_demanda VARCHAR(255),
  data_desejada DATETIME,
  numero_rds INT,
  ambiente VARCHAR(255),
  justificativa TEXT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci;
